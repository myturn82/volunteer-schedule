export function getKoreanHolidayName(dateStr: string): string | null {
  const parts = dateStr.split('-').map(Number)
  const year = parts[0], month = parts[1], day = parts[2]

  const solar = SOLAR_HOLIDAYS.find(([m, d]) => m === month && d === day)
  if (solar) return solar[2]

  return ANNUAL_HOLIDAYS[year]?.[dateStr] ?? null
}

export function isKoreanHoliday(dateStr: string): boolean {
  return getKoreanHolidayName(dateStr) !== null
}
